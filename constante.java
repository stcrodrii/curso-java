package varia_const_opera;
public class constante {
    public static void main(String[] args) {
         
        /* ¿QUE ES UNA COSTANTE? es una espacio de memoria reservado  que no podra cambiar su valor 
        durante la ejecucion del programa*/
        /*¿POR QUE HAY QUE UTILIZAR CONSTANTES? porque a la hora de programar a veces surgue
        la necesidad de guardar datos temporalmente */

       /* ¿COMO SE CREA UNA COSTANTE EN JAVA ? utilizando la palabre clave (FINAL) , luego 
       el tipo de dato , el nombre de la costante y su valor */

       final int a = 2344 ; 
       final long b = 345345L ; 
       System.out.println("el valor de la costante es " +a);
       System.out.println("el valor de la costante long es " +b);

        
        
    }
    
}
